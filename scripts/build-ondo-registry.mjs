// Parses the Ondo GM Tokens CSV into src/js/services/ondoTokens.json — a
// compact registry the renderer can bundle. Run manually when Ondo ships a
// new catalog CSV:
//   node scripts/build-ondo-registry.mjs "<path to csv>"
//
// Only what Jarvis can actually QUERY is kept: Ethereum + BSC addresses
// (keyless EVM RPC exists for both — verified live). Solana and HyperEVM
// addresses are in the CSV but are dropped here: no Solana RPC support
// exists in this codebase, and no HyperEVM endpoint is configured. Dropping
// them is stated, not silent.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const src = process.argv[2];
if (!src) { console.error('usage: node build-ondo-registry.mjs <csv>'); process.exit(1); }

// Minimal correct CSV parser — Description fields contain quoted commas.
function parseCsv(text) {
    const rows = [];
    let row = [], field = '', inQ = false;
    for (let i = 0; i < text.length; i++) {
        const c = text[i];
        if (inQ) {
            if (c === '"') {
                if (text[i + 1] === '"') { field += '"'; i++; }
                else inQ = false;
            } else field += c;
        } else if (c === '"') inQ = true;
        else if (c === ',') { row.push(field); field = ''; }
        else if (c === '\n' || c === '\r') {
            if (c === '\r' && text[i + 1] === '\n') i++;
            row.push(field); field = '';
            if (row.some(f => f !== '')) rows.push(row);
            row = [];
        } else field += c;
    }
    if (field !== '' || row.length) { row.push(field); if (row.some(f => f !== '')) rows.push(row); }
    return rows;
}

const rows = parseCsv(readFileSync(src, 'utf-8'));
const header = rows[0];
const col = (name) => {
    const i = header.indexOf(name);
    if (i === -1) throw new Error(`column not found: ${name}`);
    return i;
};

const iName = col('Name'), iSymbol = col('Symbol'),
    iEth = col('Ethereum Deployed Address'), iBsc = col('BSC Deployed Address'),
    iStockName = col('Stock Name'), iTicker = col('Stock Ticker'),
    iType = col('Type'), iDetail = col('Type Detail');

const ADDR = /^0x[0-9a-fA-F]{40}$/;
const out = [];
let skipped = 0;

for (const r of rows.slice(1)) {
    const eth = (r[iEth] || '').trim();
    const bsc = (r[iBsc] || '').trim();
    const ticker = (r[iTicker] || '').trim();
    const symbol = (r[iSymbol] || '').trim();
    if (!ADDR.test(eth)) { skipped++; continue; } // nothing queryable without an EVM address
    out.push({
        // ticker: the underlying ("AAPL"); rows without one (USDon) fall back
        // to the on-chain symbol so they stay addressable by voice.
        k: /^[A-Z.]{1,6}$/.test(ticker) ? ticker : symbol.toUpperCase(),
        s: symbol,                                  // on-chain symbol (AAPLon)
        n: (r[iStockName] || r[iName] || '').trim(),// spoken name ("Apple")
        t: (r[iType] || '').trim(),                 // Stock | ETF | Currency
        d: (r[iDetail] || '').trim(),               // Common Stock | ADR | ...
        e: eth.toLowerCase(),
        b: ADDR.test(bsc) ? bsc.toLowerCase() : null,
    });
}

// Emitted as an ESM module, not .json: Node tests need import attributes for
// JSON while Vite does not — a .js module loads identically in both.
const dest = join(dirname(fileURLToPath(import.meta.url)), '..', 'src', 'js', 'services', 'ondoTokens.js');
writeFileSync(dest,
    '// GENERATED by scripts/build-ondo-registry.mjs — do not edit by hand.\n'
    + 'export default ' + JSON.stringify(out) + ';\n', 'utf-8');
console.log(`wrote ${out.length} tokens (${skipped} rows skipped) -> ${dest}`);
console.log(`bsc coverage: ${out.filter(t => t.b).length}/${out.length}`);
